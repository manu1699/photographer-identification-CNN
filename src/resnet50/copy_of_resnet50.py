    # Generate confusion matrix
    conf_matrix = confusion_matrix(y_true, y_pred)

    # Save confusion matrix to a file
    np.savetxt("resnet50_confusion_matrix.csv", conf_matrix, delimiter=",")

    # Plot confusion matrix
